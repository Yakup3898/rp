package day03ifstatementsnt;

public class OperatorSigns {

	public static void main(String[] args) {
		/*
		 Comparison Operators: We use <(smaller), >(greater), <=(smaller or equal), >=(greater or equal), ==(equal) !=(not equal) 
		 to compare two values in Java
		 Note: Comparison operators return boolean every time in Java
		 */
        System.out.println(5<13);//true
		System.out.println(5>13);//false
		
		System.out.println(5<=13);//true
		System.out.println(5>=5);//true
		
		//in math we use "=" but in Java we have to use "=="
		//In Java "=" means assignment operator. We use "=" when we assign a value to a variable.
		System.out.println(5 == 5);//true
		
		System.out.println(5 != 5);//false
		
		
	}

}
